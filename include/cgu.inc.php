<h1 class="countheads">Conditions générales d'utilisation - Données personnelles</h1>


<br>

<h3 class="nocount"><u>Définitions</u> :</h3><br>
<p>
    <b>L'Éditeur</b> : La personne, physique ou morale, qui édite les services de communication au public en ligne.<br>
    <b>Le Site</b> : L'ensemble des sites, pages Internet et services en ligne proposés par l'Éditeur.<br>
