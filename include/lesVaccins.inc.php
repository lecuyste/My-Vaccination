                <h2>Les vaccins en France</h2>
               <p>Jusqu’en 2017, seule la vaccination contre 3 maladies (Diphtérie, Tétanos, Polio) étaient obligatoires . Les vaccins contenant ces valences étaient obligatoires, non pas du fait de l'importance des maladies qu'ils préviennent, mais parce qu'ils étaient les plus anciens*.
                En 2018, 11 valences (les 3 précédentes, ainsi que Coqueluche, Haemophilus b, Hépatite B, Pneumocoque, Méningocoque C, Rougeole, Oreillons, Rubéole) seront rendues obligatoires pour les moins de 24 mois. Ces vaccins qui étaient seulement recommandés antérieurement ont une importance au moins aussi grande, mais ont été généralement disponibles plus tard dans l'histoire de la vaccination. Il était apparu aux législateurs et autorités de santé plus important de convaincre que de contraindre. L’importance de la défiance vaccinale en France a conduit les autorités de santé à prendre transitoirement la décision d’obligation accompagnée d’une information et d’une communication renforcée vis à vis du public et des professionnels de santé.</p>

                <p>Les Vaccins recommandés pour tous en population générale : les 11 valences ci-dessus, ainsi que les vaccins DTCaP à 6 ans et dTcaP à 11 ans et pour le cocooning, les vaccins contre les papillomavirus pour les filles entre 11 ans et 14 ans. </p>

               <p> Tous les vaccins obligatoires ou recommandés sont remboursés</p>

                <p>Certains vaccins sont recommandés en cas de risques élevés d’être exposé à une maladie (tuberculose, hépatite A, meningites B ou ACYW, fièvre jaune, grippe, encéphalite à tique...), par exemple du fait d'une vulnérabilité liée à leur état de santé, au cours d’un voyage ou du fait de leurs professions (obligatoires dans certaines professions notamment de santé).</p>


               <p> Les Vaccins complémentaires sont destinés à ceux qui souhaitent une protection optimale (rotavirus, varicelle, meningites B ou ACYW, hépatite A, grippe)
                D'autres vaccins, enfin, ne sont que rarement utilisés ou ne sont plus disponibles – mais figurent peut-être dans votre carnet de vaccination ! </p>
