           <h2>Présentation de l'outil carnet : </h2>
            <p>Un carnet de santé est un document qui contient les éléments d'information médicale nécessaires au suivi de la santé et du bon développement de l'enfant jusqu’à ses 18 ans. En France, il est remis aux parents à la maternité le jour de la naissance de l’enfant. Il est rempli par la famille et les professionnels de santé. Plusieurs pages sont consacrées aux vaccinations.

                Grâce au carnet de santé, les médecins généralistes et spécialistes, les sages-femmes ou les infirmières scolaires peuvent vérifier si les vaccins obligatoires ou recommandés pour votre enfant ont été faits et si des rappels sont nécessaires.

                Les professionnels qui vaccinent l’enfant doivent le remplir obligatoirement.

                Le carnet de santé est un document médical confidentiel couvert par le secret médical. En dehors des professionnels de santé, nul ne peut en exiger la communication (Code de la santé publique).

                Pour les enfants, le carnet de santé sert de carnet de vaccination. Le carnet de vaccination peut remplacer le carnet de santé lorsque l’on est adulte.</p>

