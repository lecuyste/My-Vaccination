           <h1>Présentation de l'equipe : </h1>
<p>NfactoLab est une ligne directe d’information et de consultation sur les vaccinations créée en Janvier 1997 en collaboration avec ACTIV et le Groupe de Pathologie Infectieuse Pédiatrique de la Société Française de Pédiatrie.

    Les informations répertoriées ici ont été sélectionnées pour leur objectivité et leurs valeurs médicale et scientifique. Elles s’appuient essentiellement sur les recommandations officielles de vaccinations en France et, à défaut, sur des études scientifiques et/ou des articles publiés. Néanmoins, elles ne remplacent pas les conseils personnalisés de votre médecin, qui est celui qui connaît le mieux votre situation : n’hésitez donc pas à le contacter !

    NfactoLab ne se substitue pas aux autorités de santé. Les recommandations et le calendrier vaccinal sont faits par le Comité Technique des Vaccinations (CTV), la Haute Autorité de Santé (HAS) et le Haut Conseil de Santé Publique (HCSP) et InfoVac n’émet aucune recommandation collective. Il fournit une aide à la vaccination individuelle des patients en fonction de leurs antécédents personnels et familiaux. De plus, InfoVac relaie et génère des informations vaccinales et relève parfois des incohérences dans les recommandations actuelles et en réfère aux autorités officielles.

    NfactoLab, c’est un réseau d’experts qui se sont donnés pour mission de répondre rapidement aux questions liées aux vaccinations que se posent les médecins. Les consultants d'InfoVac sont des pédiatres avec une expertise particulière dans le domaine des maladies infectieuses et des vaccinations. Ils travaillent en réseau afin de garantir compétence, crédibilité, objectivité et homogénéité des réponses.

    Actuellement, Stéphane Lecuyer et Samuel Lacaille coordonnent les experts NfactoLab</p>
